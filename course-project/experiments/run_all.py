#!/usr/bin/env python3
"""
run_all.py — One-click experiment runner for dEA vs HdEA
Each run creates a timestamped output directory, never overwrites old data.

Output structure:
  experiments/output/run_20260713_194200_pcb442/
    params.txt                      ← experiment configuration
    interval_<N>/<algorithm>/
      results.txt                   ← run best elapsed
      run_0_conv.txt ...            ← convergence traces
    analysis/                       ← generated by analyze.py

Usage:
  python run_all.py [--procs 64] [--runs 10] [--intervals 100]
"""

import subprocess
import os
import sys
import argparse
import time
from datetime import datetime

PROJECT_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
OUTPUT_BASE = os.path.join(PROJECT_DIR, "experiments", "output")

ALGORITHMS = {
    "dEA": {
        "dir": "04_dEA_island", "exe": "TSP_dEA_island.exe",
        "extra_args": [],
    },
    "HdEA_RingIndividual": {
        "dir": "05_HdEA", "exe": "TSP_HdEA.exe",
        "extra_args": ["0"],
    },
    "HdEA_RingColony": {
        "dir": "05_HdEA", "exe": "TSP_HdEA.exe",
        "extra_args": ["2"],
    },
}

def get_group_layout(procs):
    layouts = {4: (2, 2), 8: (2, 4), 9: (3, 3), 16: (4, 4),
               36: (6, 6), 64: (8, 8)}
    if procs in layouts:
        return layouts[procs]
    for m in range(int(procs ** 0.5), 0, -1):
        if procs % m == 0:
            return (m, procs // m)
    return (1, procs)


def main():
    parser = argparse.ArgumentParser(
        description="Run dEA vs HdEA experiments")
    parser.add_argument("--instances", default="pcb442",
                        help="comma-separated instance names")
    parser.add_argument("--algorithms",
                        default="dEA,HdEA_RingIndividual,HdEA_RingColony",
                        help="algorithms to run")
    parser.add_argument("--runs", type=int, default=10)
    parser.add_argument("--procs", type=int, default=64)
    parser.add_argument("--intervals", default="100")
    args = parser.parse_args()

    M, N = get_group_layout(args.procs)
    instances = [s.strip() for s in args.instances.split(",")]
    algs = [s.strip() for s in args.algorithms.split(",")]
    intervals = [int(x) for x in args.intervals.split(",")]

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    run_name = f"run_{timestamp}_{'_'.join(instances)}"
    run_dir = os.path.join(OUTPUT_BASE, run_name)

    tsp_dir = os.path.join(PROJECT_DIR, "tsp_instances")

    jobs = []
    for inst in instances:
        for alg_name in algs:
            alg = ALGORITHMS[alg_name]
            for mig_int in intervals:
                out_dir = os.path.join(
                    run_dir, f"interval_{mig_int}", alg_name)
                os.makedirs(out_dir, exist_ok=True)
                jobs.append((alg_name, alg, inst, mig_int, out_dir, M, N))

    # Write experiment config for reproducibility
    with open(os.path.join(run_dir, "params.txt"), "w") as f:
        f.write(f"timestamp: {timestamp}\n")
        f.write(f"instances: {instances}\n")
        f.write(f"algorithms: {algs}\n")
        f.write(f"processes: {args.procs} (M={M}, N={N})\n")
        f.write(f"runs_per_combo: {args.runs}\n")
        f.write(f"intervals: {intervals}\n")
        f.write(f"population_size: 100\n")
        f.write(f"local_ratio: 20 (local:global migration, HdEA only)\n")
        f.write(f"data_source: {tsp_dir}\n")

    print(f"{'='*60}")
    print(f"Experiment: {run_name}")
    print(f"  Instances: {instances}")
    print(f"  Algorithms: {algs}")
    print(f"  Procs: {args.procs} (M={M}, N={N})")
    print(f"  Runs per combo: {args.runs}")
    print(f"  Intervals: {intervals}")
    print(f"  Total jobs: {len(jobs)}")
    print(f"  Output: {run_dir}")
    print(f"{'='*60}")

    for idx, (alg_name, alg, inst, mig_int, out_dir, M, N) in enumerate(jobs):
        tsp_path = os.path.join(tsp_dir, f"{inst}.tsp")
        exe_path = os.path.join(PROJECT_DIR, alg["dir"], alg["exe"])
        workdir = os.path.join(PROJECT_DIR, alg["dir"])

        cmd = ["mpiexec", "-n", str(args.procs), "-wdir", workdir, exe_path,
               tsp_path]

        if alg_name == "dEA":
            cmd += [str(args.runs),
                    str(mig_int * 20 * 100),
                    out_dir]
        else:
            cmd += [str(M), str(N), str(args.runs),
                    str(mig_int)] + alg["extra_args"] + [out_dir]

        t0 = time.time()
        tag = f"[{idx+1}/{len(jobs)}] {inst}/{alg_name} mig={mig_int}"
        print(f"{tag}  [{datetime.now().strftime('%H:%M:%S')}]", flush=True)

        try:
            r = subprocess.run(cmd, cwd=workdir, capture_output=True,
                               text=True, timeout=7200)
            elapsed = time.time() - t0
            ok = r.returncode == 0
            status = "OK" if ok else f"FAIL(rc={r.returncode})"
            print(f"  {status}  {elapsed:.0f}s", flush=True)
            if not ok:
                print(f"  stderr: {r.stderr[:200]}")
        except subprocess.TimeoutExpired:
            print(f"  TIMEOUT  {time.time()-t0:.0f}s", flush=True)
        except Exception as e:
            print(f"  ERROR: {e}", flush=True)

    print(f"\nDone. Output in: {run_dir}")


if __name__ == "__main__":
    main()
